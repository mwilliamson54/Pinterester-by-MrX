/**
 * BulkyGen XMP Serializer
 * Serializes mapped metadata into native XML (APP1 segment).
 */
(function() {
    'use strict';

    /**
     * Escape XML special characters.
     */
    function escapeXml(unsafe) {
        if (typeof unsafe !== 'string') return '';
        return unsafe.replace(/[<>&'"]/g, (c) => {
            switch (c) {
                case '<': return '&lt;';
                case '>': return '&gt;';
                case '&': return '&amp;';
                case '\'': return '&apos;';
                case '"': return '&quot;';
                default: return c;
            }
        });
    }

    /**
     * Generate XMP XML string from mapped XMP dictionary.
     * @param {Object} mapped - Dictionary generated by metadata-mapper (e.g. { 'dc:title': '...', 'dc:subject': [...] })
     * @returns {string} XML string
     */
    function serialize(mapped) {
        let xml = `<?xpacket begin="﻿" id="W5M0MpCehiHzreSzNTczkc9d"?>\n`;
        xml += `<x:xmpmeta xmlns:x="adobe:ns:meta/">\n`;
        xml += ` <rdf:RDF xmlns:rdf="http://www.w3.org/1999/02/22-rdf-syntax-ns#"\n`;
        xml += `          xmlns:dc="http://purl.org/dc/elements/1.1/"\n`;
        xml += `          xmlns:xmp="http://ns.adobe.com/xap/1.0/"\n`;
        xml += `          xmlns:xmpRights="http://ns.adobe.com/xap/1.0/rights/"\n`;
        xml += `          xmlns:photoshop="http://ns.adobe.com/photoshop/1.0/"\n`;
        xml += `          xmlns:xmpMM="http://ns.adobe.com/xap/1.0/mm/">\n`;
        xml += `  <rdf:Description rdf:about="">\n`;

        // We process the mapped dict.
        for (const [key, value] of Object.entries(mapped)) {
            // Arrays (like keywords)
            if (Array.isArray(value)) {
                // Determine if it's a Bag, Seq, or Alt. dc:subject is Bag. dc:creator is Seq.
                const containerType = key === 'dc:creator' ? 'Seq' : 'Bag';
                xml += `   <${key}>\n    <rdf:${containerType}>\n`;
                for (const item of value) {
                    xml += `     <rdf:li>${escapeXml(String(item))}</rdf:li>\n`;
                }
                xml += `    </rdf:${containerType}>\n   </${key}>\n`;
            } 
            // Lang Alt (like title, description, copyright)
            else if (['dc:title', 'dc:description', 'dc:rights'].includes(key)) {
                xml += `   <${key}>\n    <rdf:Alt>\n     <rdf:li xml:lang="x-default">${escapeXml(String(value))}</rdf:li>\n    </rdf:Alt>\n   </${key}>\n`;
            }
            // Simple strings
            else {
                xml += `   <${key}>${escapeXml(String(value))}</${key}>\n`;
            }
        }

        xml += `  </rdf:Description>\n`;
        xml += ` </rdf:RDF>\n`;
        xml += `</x:xmpmeta>\n`;
        xml += `<?xpacket end="w"?>`;
        
        return xml;
    }

    /**
     * Embed the XMP XML into a JPEG blob as an APP1 segment.
     */
    async function embed(jpegBlob, xmlString) {
        if (!xmlString) return jpegBlob;

        const arrBuffer = await jpegBlob.arrayBuffer();
        const jpegBytes = new Uint8Array(arrBuffer);

        if (jpegBytes[0] !== 0xFF || jpegBytes[1] !== 0xD8) {
            throw new Error('Input is not a valid JPEG (missing SOI marker)');
        }

        const xmpBytes = new TextEncoder().encode(xmlString);
        const namespaceBytes = new TextEncoder().encode('http://ns.adobe.com/xap/1.0/\u0000');
        const payloadLen = namespaceBytes.length + xmpBytes.length;
        const markerLen = payloadLen + 2; 

        // Segment header: marker (FFE1) + 2 length bytes
        const segmentHeader = new Uint8Array(4);
        segmentHeader[0] = 0xFF;
        segmentHeader[1] = 0xE1; // APP1
        segmentHeader[2] = (markerLen >>> 8) & 0xFF;
        segmentHeader[3] = markerLen & 0xFF;

        const before = jpegBytes.subarray(0, 2);
        const after = jpegBytes.subarray(2);

        const outputBytes = new Uint8Array(
            before.length + segmentHeader.length + namespaceBytes.length + xmpBytes.length + after.length
        );

        let offset = 0;
        outputBytes.set(before, offset); offset += before.length;
        outputBytes.set(segmentHeader, offset); offset += segmentHeader.length;
        outputBytes.set(namespaceBytes, offset); offset += namespaceBytes.length;
        outputBytes.set(xmpBytes, offset); offset += xmpBytes.length;
        outputBytes.set(after, offset);

        return new Blob([outputBytes], { type: 'image/jpeg' });
    }

    globalThis.bulkygenXmpSerializer = {
        serialize,
        embed
    };
})();
